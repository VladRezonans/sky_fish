//---------------------------------------------------------------------------------------------------------------------------------------------------
extend(tSmartMissileIcon, tSmartMissile);
//---------------------------------------------------------------------------------------------------------------------------------------------------
function tSmartMissileIcon(params) {
	this.type = 'smartMissileIcon';
	this.status = 'norm';
	this.r = 4;
	this.color = '#226622';
		
	this.x = 300.0;
	this.y = 300.0;
		
	this.a = Math.PI;
	this.da = 0.02;

	this.v = 0.09;
	
	this.setParam(params);		
}
//---------------------------------------------------------------------------------------------------------------------------------------------------
tSmartMissileIcon.prototype.checkTargets = function() {	
}
//----------------------------------------------------------------------------------------------------------------------------------------------------
tSmartMissileIcon.prototype.engine = function() {
	this.a += this.da;

	if (this.a > 2.0 * Math.PI) this.a = this.a - 2.0 * Math.PI;
	if (this.a < 0) this.a = this.a + 2.0 * Math.PI;
}
//---------------------------------------------------------------------------------------------------------------------------------------------------
tSmartMissileIcon.prototype.show = function() {		
	this.shablon(this.x, this.y, this.a);	
}
//---------------------------------------------------------------------------------------------------------------------------------------------------
tSmartMissileIcon.prototype.bang = function() {
}
