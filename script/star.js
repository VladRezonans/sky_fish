//---------------------------------------------------------------------------------------------------------------------------------------------------
extend(tStar, tShape);
//---------------------------------------------------------------------------------------------------------------------------------------------------	
function tStar() {
	this.type = 'star';
	this.status = 'norm'
	this.group = 'stars';
	this.color = '#AAAAFF';
}
//---------------------------------------------------------------------------------------------------------------------------------------------------
tStar.prototype.shablon = function(x, y, color) {
	ctx.beginPath();
	ctx.arc(x, y, 1, 0, 2 * Math.PI);
	ctx.fillStyle = color;
	ctx.fill();
}
//---------------------------------------------------------------------------------------------------------------------------------------------------
tStar.prototype.show = function() {
	var x = (this.x - sceneX)/this.z;
	var y = (this.y - sceneY)/this.z;
	
	this.shablon(x, y, this.color);
}
