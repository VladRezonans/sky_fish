//---------------------------------------------------------------------------------------------------------------------------------------------------
extend(tMegaMissileIcon, tMegaMissile);
//---------------------------------------------------------------------------------------------------------------------------------------------------
function tMegaMissileIcon(params) {
	this.type = 'megaMissileIcon';
	this.status = 'norm';
	this.r = 12;
	this.color = '#226622';
	
	this.x = 300;
	this.y = 300;
	
	this.a = Math.PI/2.0;
	this.da = 0.002;

	this.v = 0.08;	
	
	this.setParam(params);	
}
//---------------------------------------------------------------------------------------------------------------------------------------------------
tMegaMissileIcon.prototype.checkTargets = function() {	
}
//----------------------------------------------------------------------------------------------------------------------------------------------------
tMegaMissileIcon.prototype.engine = function() {
	this.a += this.da;	

	if (this.a > 2.0 * Math.PI) this.a = this.a - 2.0 * Math.PI;
	if (this.a < 0) this.a = this.a + 2.0 * Math.PI;
}
//---------------------------------------------------------------------------------------------------------------------------------------------------
tMegaMissileIcon.prototype.show = function() {		
	this.shablon(this.x, this.y, this.a);	
}
//---------------------------------------------------------------------------------------------------------------------------------------------------
tMegaMissileIcon.prototype.bang = function() {
}
