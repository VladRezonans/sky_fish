//---------------------------------------------------------------------------------------------------------------------------------------------------
extend(tHeavyMissileIcon, tHeavyMissile);
//---------------------------------------------------------------------------------------------------------------------------------------------------
function tHeavyMissileIcon(params) {
	this.type = 'tHeavyMissileIcon';
	this.status = 'norm';
	this.r = 8;
	this.color = '#226622';
	
	this.x = 300;
	this.y = 300;
	
	this.a = Math.PI;
	this.da = 0.004;

	this.v = 0.05;	
	
	this.setParam(params);	
}
//---------------------------------------------------------------------------------------------------------------------------------------------------
tHeavyMissileIcon.prototype.checkTargets = function() {	
}
//----------------------------------------------------------------------------------------------------------------------------------------------------
tHeavyMissileIcon.prototype.engine = function() {
	this.a += this.da;	

	if (this.a > 2.0 * Math.PI) this.a = this.a - 2.0 * Math.PI;
	if (this.a < 0) this.a = this.a + 2.0 * Math.PI;
}
//---------------------------------------------------------------------------------------------------------------------------------------------------
tHeavyMissileIcon.prototype.show = function() {		
	this.shablon(this.x, this.y, this.a);	
}
//---------------------------------------------------------------------------------------------------------------------------------------------------
tHeavyMissileIcon.prototype.hide = function() {	
}
//---------------------------------------------------------------------------------------------------------------------------------------------------
tHeavyMissileIcon.prototype.bang = function() {
}
